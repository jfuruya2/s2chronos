
DROP TABLE EVENT;
CREATE TABLE EVENT(
EVENT_ID		IDENTITY	PRIMARY KEY,
EVENT_DATE 		TIMESTAMP,
EVENT_TEXT		VARCHAR(255),
EVENT_STATUS	INT
);

DROP TABLE USER;
CREATE TABLE USER(
USER_ID		IDENTITY	PRIMARY KEY,
USER_NAME	VARCHAR(255),
LAST_NAME	VARCHAR(255),
FIRST_NAME	VARCHAR(255),
EMAIL		VARCHAR(64),
USER_STATUS	INT
);
