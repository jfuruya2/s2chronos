package org.seasar.chronos.exception;

public class InvalidNextJobMethodException extends Exception {

	private static final long serialVersionUID = -2342865858082537369L;

	public InvalidNextJobMethodException() {
	}

	public InvalidNextJobMethodException(String arg0) {
		super(arg0);
	}

	public InvalidNextJobMethodException(Throwable arg0) {
		super(arg0);
	}

	public InvalidNextJobMethodException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
